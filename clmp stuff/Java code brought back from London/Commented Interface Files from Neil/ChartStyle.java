import java.awt.*;

/**
 * Should really be called LineStyle: how a single line looks.
 */

class ChartStyle
{
     
    /**
    * the thickness of the lines
    */
    private int thickness;
    
    /**
    * whether the lines are solid, dotted, a different type of dotted, etc.
    */
    private int type;
    
    /**
    *  the lines color
    */
    private Color theColor;
    
    /**
    * the style of the actual point. not needed yet
    */
    private int PointStyle;
    
}
    
